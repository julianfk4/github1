public class Coche {
    //atributos
    String marca, modelo, color;
    double velocidad, velocidadMax;

    //metodo constructor
    public Coche (String marca, String modelo, String color, double velocidad, double velocidadMax){
        this.marca=marca;
        this.modelo=modelo;
        this.color=color;  
        this.velocidad=velocidad;
        this.velocidadMax=velocidadMax;
    }
}
